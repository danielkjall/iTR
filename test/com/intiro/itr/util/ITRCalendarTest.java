  @Test
  public void testNextWeekPart() {
    // Årsskifte 2016-2017    
    ITRCalendar instance = new ITRCalendar("2016-12-19");
    instance.nextWeekPart();
    instance.getFromDate();
    assertEquals(instance.getFromDate().getCalendarInStoreFormat(), "2016-12-26");
    assertEquals(instance.getToDate().getCalendarInStoreFormat(), "2016-12-31");
    assertEquals(instance.getWeekOfYear(), 52);
    assertEquals(instance.getWeekPart(), 1);
    
    instance.nextWeekPart();
    instance.getFromDate();
    assertEquals(instance.getFromDate().getCalendarInStoreFormat(), "2017-01-01");
    assertEquals(instance.getToDate().getCalendarInStoreFormat(), "2017-01-01");
    assertEquals(instance.getWeekOfYear(), 52);
    assertEquals(instance.getWeekPart(), 2);
    
    // Årsskifte 2017-2018
    instance = new ITRCalendar("2017-12-19");
    instance.nextWeekPart();
    assertEquals(instance.getFromDate().getCalendarInStoreFormat(), "2017-12-25");
    assertEquals(instance.getToDate().getCalendarInStoreFormat(), "2017-12-31");
    assertEquals(instance.getWeekOfYear(), 52);

    instance.nextWeekPart();
    assertEquals(instance.getFromDate().getCalendarInStoreFormat(), "2018-01-01");
    assertEquals(instance.getToDate().getCalendarInStoreFormat(), "2018-01-07");
    assertEquals(instance.getWeekOfYear(), 1);
    
  }

